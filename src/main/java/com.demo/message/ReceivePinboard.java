package com.demo.message;

public class ReceivePinboard {

    private int index;

    private int[] sn;

    private int snAsInt;

    private String snAsString;

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public int[] getSn() {
        return sn;
    }

    public void setSn(int[] sn) {
        this.sn = sn;
    }

    public int getSnAsInt() {
        return snAsInt;
    }

    public void setSnAsInt(int snAsInt) {
        this.snAsInt = snAsInt;
    }

    public String getSnAsString() {
        return snAsString;
    }

    public void setSnAsString(String snAsString) {
        this.snAsString = snAsString;
    }
}
